if [[ "$EUID" = 0 ]]; then
    echo "(1) already root"
else
    sudo -k # make sure to ask for password on next sudo
    if sudo true; then
        echo "(2) correct password"
    else
        echo "(3) wrong password"
        exit 1
    fi
fi

if (( $# == 2 )); then
    sudo mount -t tmpfs -o size="$2"M tmpfs "$1"
else
    echo "Usage: ${0##*/} <path to mount> <size (in MB/Mo)>"
fi
